async function importATKProducts() {
    try {
        // Inisialisasi database (override path di database.js)
        const db = new Database();

        console.log('Starting import of ATK products...');

        // Validasi dan masukkan produk
        for (const product of products) {
            try {
                // Validasi data
                if (!product.name || !['atk', 'makmin'].includes(product.type) || 
                    isNaN(product.purchase_price) || isNaN(product.selling_price) || isNaN(product.stock)) {
                    console.warn(`Skipping invalid product: ${JSON.stringify(product)}`);
                    continue;
                }

                await db.addProduct({
                    name: product.name.trim(),
                    type: product.type.trim().toLowerCase(),
                    purchase_price: parseFloat(product.purchase_price),
                    selling_price: parseFloat(product.selling_price),
                    stock: parseInt(product.stock)
                });
                console.log(`Added: ${product.name} (stock: ${product.stock})`);
            } catch (err) {
                console.error(`Error adding ${product.name}:`, err.message);
            }
        }

        // Tutup database
        db.close();
        console.log('Import completed. Total products processed:', products.length);
    } catch (err) {
        console.error('Error during import:', err.message);
    }
}

